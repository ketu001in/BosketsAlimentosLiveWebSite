      <div style="display:flex;gap:20px;justify-content:center;margin:20px 0;flex-wrap:wrap">
        <div class="stat-card"><strong style="color:var(--green-700)"><?= (int)$_GET['imported'] ?></strong><span>Imported</span></div>
