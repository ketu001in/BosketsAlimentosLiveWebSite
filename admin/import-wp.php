      <div class="table-wrap">
        <table class="data">
          <tr><th>Title</th><th>Category</th><th>Ingredients</th><th>Steps</th><th>Image</th></tr>
          <?php foreach (array_slice($preview['recipes'], 0, 5) as $r): ?>
            <tr>
