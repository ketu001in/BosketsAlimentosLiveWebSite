<?php
/**
 * Admin: Import WordPress recipes from exported ZIP.
 * Upload ZIP via this page → recipes go to 'pending' status.
 */
// Raise limits for large ZIP uploads before anything else
@ini_set('upload_max_filesize', '256M');
@ini_set('post_max_size',       '256M');
@ini_set('memory_limit',        '512M');
@ini_set('max_execution_time',  '300');

require_once __DIR__ . '/_admin.php';
ensure_recipe_pending_status();

$results = null;
$preview = null;

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    csrf_check();
    $action = $_POST['action'] ?? '';

    // ── Preview ZIP before import ────────────────────────────────────────────
    if ($action === 'preview' && !empty($_FILES['zipfile']['name'])) {
        $tmpZip = $_FILES['zipfile']['tmp_name'];
        $zip = new ZipArchive();
        if ($zip->open($tmpZip) !== true) {
            flash('error', 'Could not open ZIP file. Make sure it was generated by the WP Exporter.');
        } else {
            $jsonContent = $zip->getFromName('wp-recipes.json');
            $zip->close();
            if (!$jsonContent) {
                flash('error', 'wp-recipes.json not found in ZIP. Please re-export.');
            } else {
                $data    = json_decode($jsonContent, true);
                $preview = $data;
                // Store ZIP temporarily for the actual import
                $tmpPath = sys_get_temp_dir() . '/ba_wp_import.zip';
                move_uploaded_file($tmpZip, $tmpPath);
                $_SESSION['_wp_import_tmp'] = $tmpPath;
                $_SESSION['_wp_import_ts']  = time();
            }
        }
    }

    // ── Run actual import ────────────────────────────────────────────────────
    if ($action === 'import') {
        csrf_check();
        $tmpPath = $_SESSION['_wp_import_tmp'] ?? '';
        if (!$tmpPath || !file_exists($tmpPath) || (time() - ($_SESSION['_wp_import_ts'] ?? 0)) > 3600) {
            flash('error', 'Import session expired. Please upload the ZIP again.');
            redirect('admin/import-wp.php');
        }

        set_time_limit(300);
        $zip = new ZipArchive();
        if ($zip->open($tmpPath) !== true) { flash('error', 'Could not open ZIP.'); redirect('admin/import-wp.php'); }

        $jsonContent = $zip->getFromName('wp-recipes.json');
        $data        = json_decode($jsonContent, true);
        $recipes     = $data['recipes'] ?? [];

        // Admin user ID
        $adminId = (int)db()->query("SELECT id FROM users WHERE is_admin=1 ORDER BY id LIMIT 1")->fetchColumn();

        $imported = 0; $skipped = 0; $failed = 0;
        $pdo = db();

        foreach ($recipes as $r) {
            // Duplicate check by title
            $dup = $pdo->prepare("SELECT COUNT(*) FROM recipes WHERE title=?");
            $dup->execute([trim($r['title'])]);
            if ($dup->fetchColumn() > 0) { $skipped++; continue; }

            // Handle image
            $imgPath = null;
            if (!empty($r['image'])) {
                $imgData = $zip->getFromName($r['image']);
                if ($imgData) {
                    $ext      = pathinfo($r['image'], PATHINFO_EXTENSION) ?: 'jpg';
                    $imgName  = 'wp_' . ($r['wp_id'] ?? uniqid()) . '.' . $ext;
                    $imgDir   = dirname(__DIR__) . '/uploads/recipes/';
                    if (!is_dir($imgDir)) mkdir($imgDir, 0755, true);
                    file_put_contents($imgDir . $imgName, $imgData);
                    $imgPath = 'uploads/recipes/' . $imgName;
                }
            }

            // Category
            $catId = null;
            if (!empty($r['category'])) {
                $catId = find_or_create('categories', $r['category'], $adminId);
            }

            // Slug
            $slug = slugify($r['title']);
            $slugCheck = $pdo->prepare("SELECT COUNT(*) FROM recipes WHERE slug LIKE ?");
            $slugCheck->execute([$slug . '%']);
            if ($slugCheck->fetchColumn() > 0) $slug .= '-' . substr(bin2hex(random_bytes(2)), 0, 4);

            // Story
            $story = $r['story'] ?? '';

            try {
                $pdo->beginTransaction();

                // Insert recipe
                $prepTime = (int)($r['prep_time'] ?? 0) ?: null;
                $cookTime = (int)($r['cook_time'] ?? 0) ?: null;
                $cuiId    = !empty($r['cuisine']) ? find_or_create('cuisines', $r['cuisine'], $adminId) : null;
                $pdo->prepare(
                    "INSERT INTO recipes (user_id, title, slug, image, story, prep_time, cook_time, category_id, cuisine_id, status, created_at)
                     VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, 'pending', ?)"
                )->execute([$adminId, $r['title'], $slug, $imgPath, $story ?: null,
                    $prepTime, $cookTime, $catId, $cuiId,
                    $r['published_at'] ?? date('Y-m-d H:i:s')]);
                $recipeId = (int)$pdo->lastInsertId();

                // Insert ingredients
                foreach (($r['ingredients'] ?? []) as $order => $ingLine) {
                    $ingLine = trim($ingLine);
                    if (!$ingLine) continue;
                    // Try to split "200g Paneer" into qty + name
                    if (preg_match('/^([\d\/\.\s]+(?:g|kg|ml|l|cup|tbsp|tsp|piece|pinch|handful|medium|large|small)[s]?\.?)\s+(.+)$/i', $ingLine, $m)) {
                        $qty  = trim($m[1]);
                        $name = trim($m[2]);
                    } else {
                        $qty  = '';
                        $name = $ingLine;
                    }
                    $ingId = find_or_create('ingredients', $name, $adminId);
                    if ($ingId) {
                        $pdo->prepare("INSERT INTO recipe_ingredients (recipe_id, ingredient_id, quantity, sort_order) VALUES (?,?,?,?)")
                            ->execute([$recipeId, $ingId, $qty ?: null, $order]);
                    }
                }

                // Insert steps
                foreach (($r['steps'] ?? []) as $n => $stepText) {
                    $stepText = trim($stepText);
                    if (!$stepText) continue;
                    $pdo->prepare("INSERT INTO recipe_steps (recipe_id, step_no, instruction) VALUES (?,?,?)")
                        ->execute([$recipeId, $n + 1, $stepText]);
                }

                $pdo->commit();
                $imported++;
            } catch (Throwable $e) {
                $pdo->rollBack();
                $failed++;
            }
        }

        $zip->close();
        unlink($tmpPath);
        unset($_SESSION['_wp_import_tmp'], $_SESSION['_wp_import_ts']);

        $results = compact('imported', 'skipped', 'failed');
        flash('success', "Import complete: $imported imported, $skipped skipped (duplicates), $failed failed.");
        redirect('admin/import-wp.php?done=1&imported=' . $imported . '&skipped=' . $skipped . '&failed=' . $failed);
    }
}

// Done redirect
$done = isset($_GET['done']);

$pageTitle = 'Admin · Import WordPress';
