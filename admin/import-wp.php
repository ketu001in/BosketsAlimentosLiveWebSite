        <button class="btn btn-primary" type="submit"
                onclick="this.textContent='Importing... please wait ⏳ (may take 1-2 min)';this.disabled=true;this.form.submit()">
          🚀 Start Import (<?= (int)$preview['total'] ?> recipes)
        </button>
        <a href="admin/import-wp.php" class="btn btn-ghost" style="margin-left:10px">Cancel</a>
      </form>
    </div>

  <?php else: ?>
    <div class="panel" style="max-width:600px">
      <h3>Step 1 — Run the exporter on your local machine</h3>
      <ol style="line-height:2.2">
        <li>Copy <code>wp-exporter.php</code> to <code>C:\xampp\htdocs\boskets-old\</code></li>
        <li>Visit <a href="http://localhost/boskets-old/wp-exporter.php" target="_blank">http://localhost/boskets-old/wp-exporter.php</a></li>
