        <div class="stat-card"><strong style="color:var(--danger)"><?= (int)$_GET['failed'] ?></strong><span>Failed</span></div>
      </div>
      <p class="muted">All imported recipes are in <strong>Pending</strong> status. Review and bulk-approve them from the Content page.</p>
      <a href="<?= e(url('admin/content.php?tab=pending')) ?>" class="btn btn-primary" style="margin-top:8px">Review Pending Recipes →</a>
    </div>

  <?php elseif ($preview): ?>
    <div class="panel">
      <h3>Preview — Ready to Import</h3>
      <div style="display:flex;gap:16px;margin-bottom:20px;flex-wrap:wrap">
        <div class="stat-card"><strong><?= (int)$preview['total'] ?></strong><span>Total recipes</span></div>
        <div class="stat-card"><strong><?= count(array_filter($preview['recipes'], fn($r) => !empty($r['image']))) ?></strong><span>With images</span></div>
        <div class="stat-card"><strong><?= count(array_filter($preview['recipes'], fn($r) => count($r['ingredients']) > 0)) ?></strong><span>With ingredients</span></div>
